import UIKit

extension UIColor {
    static var lightBackground: UIColor {
        return UIColor(red: 0.9, green: 0.9, blue: 0.9, alpha: 1)
    }
    static var darkBackground: UIColor {
        return UIColor(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    static var subLightBackground: UIColor {
        return UIColor(red: 0.8, green: 0.8, blue: 0.8, alpha: 1)
    }
    static var subDarkBackground: UIColor {
        return UIColor(red: 0.1, green: 0.1, blue: 0.1, alpha: 1)
    }
    static var lightTint: UIColor {
        return UIColor(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    static var darkTint: UIColor {
        return UIColor(red: 0.8, green: 0.8, blue: 0.8, alpha: 1)
    }
}
